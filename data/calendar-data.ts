export function getCategoryById(id: string): Category | undefined {
  return categories.find((cat) => cat.id === id);
}

export function getSemesterById(id: string): Semester | undefined {
  return semesters.find((sem) => sem.id === id);
}

export function getEventsBySemester(semester: string): CalendarEvent[] {
  if (semester === "all") return calendarEvents;
  return calendarEvents.filter(
    (event) => event.semester === semester || event.semester === "general"
  );
}

export function getEventsByCategory(category: string): CalendarEvent[] {
  return calendarEvents.filter((event) => event.category === category);
}
